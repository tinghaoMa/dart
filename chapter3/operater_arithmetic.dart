void main() {
  int a = 2;
  int b = 10;
  print(a + b);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a ~/ b);
  print(a % b);

  print(a++);
  print(a);
  print(++a);

  print(a--);
  print(a);
  print(--a);


}
